					<p>An average, a family in my village makes less than $1 a day. Most households have to save for no less than three months to have $10. Say the harvest was good and they saved $20, they will have to spread this around. For instance: 5-10 notebooks (elementary school), a pair of uniform, a pen or pencil then use the rest of the year to try and make money for school fees ( $30 or more depending on the school). Some schools allow parents to pay the school fee in two or three installments but if they do not finish paying by February-March, the kid is sent home. After this has happened to a student a few times, most of them get frustrated and drop out thus ending their education. Other reasons kids drop out is because of a handicap, like the story of Miriamu born with no arms or the story of Ngong Jespa and Shebom Solange from an accident. It is amazing that these kids keep striving to achieve an education. They deserved your help. Please visit our website and click on the Donate button to help out these kids or Sponsor a Child from our list. Your help of any kind is greatly appreciated.</p>					<br /><br /><br />
