				<p>As many of you know, I left Cameroon in 2002 with a burning desire to help out these seven young girls among many other underprivileged in our communities. I look at these girls and my heart breaks into pieces. What have become of them? These were 12-15 year olds whose parents had no choice but to marry them off to the first suitor partly due to cultural aspects and partly because they could not afford to send them to secondary school.  I weep for these girls not only because I could not afford to support at minimum wage while sponsoring our yearly sports program but also because they like must underprivileged girls, got married and had babies before they turned 18. </p>
				<br />
				<p>Why does this particular situation bother me so much? I remember at the age of 9 a girl in my class was pulled out of school and married off to a rich man who already had many wives. Also, at the age of 13 while I was packing my bags to go to secondary schools, my dear cousin was being carried away kicking and screaming into a marriage she had no say in. She was about 15yrs old. The memory of that night still hunts me. I couldn’t help them then no matter how bad I wanted to. So when I got the opportunity, I starting thinking of ways I could change the faith of as many as there are in my community and surroundings. In ten years, I was only able to help a few stay in school and help one almost through secondary school until a suitor came by. She was able to graduate secondary school at the age of 18, giving her a fighting chance in life. Even though we lost touch, I know whatever little seed I planted made a difference in her life but I hurt for the rest of them.</p>
				<br />
				<p>Now, look at the second picture above and see the little children in the photos. Some of these children belong to young girls like the girls in picture number one—girls who don’t have an opportunity, a chance, a real shot of making something of themselves. Many of these girls have never had someone tell them that they can be more. Now, look at the older kids in the picture. When I left Cameroon in 2002 they were the children I wanted so badly to help.  In this picture most of them are at the Plant A Seed Youth Activities not for themselves but for their children. Thus goes the vicious circle. Children who bear children who turn around and bear children without much hope for what the future holds—a future they didn’t get. What will they pass on to their children?  I pray that what breaks my heart will break yours…not only for the young girls but for all underprivileged kids. </p>
				<br />
				<p>Finally, picture a child, someone close to your heart. Now imagine him/her at 10 years of age. Now, imagine him/her 10yrs later. Ten years ago, I left Cameroon seeking for opportunities to help a generation of my friends’ and cousin’s kids from falling into the same dilemma as their mothers. Ten years into this journey, I am faced with the same dilemma but slightly different—it is not for my friends’ or cousin’s kids, but rather their grandkids. This time it cannot wait another 10 years. More so, it cannot wait one more month. Please join me and together we can break this circle:</p>
